#include <stdio.h>

/**
 * main - The FizzBuzz test
 *
 *@i: Write a program that prints the numbers from 1 to 100
 * Return: (0)
 **/

int main(int i)
{
	int i;

	for (i = 1; i < 100; i++)
	{
		if ((i % 3 == 0) && (i % 5 == 0))
		{
			printf("FizzBuzz ");
		}
		else if (i % 3 == 0)
		{
			printf("Fizz ");
		}
		else if (i % 5 == 0)
		{
			printf("Buzz ");
		}
		else
		{
			printf("%d ", i);
		}
	}
	return (0);
}
